    private function decryptIn($code){
        $salt = self::__SALT;
        $key = substr(hash('sha256', $salt.$this->key.$salt), 0, 32);
        $iv_size = mcrypt_get_iv_size(MCRYPT_RIJNDAEL_256, MCRYPT_MODE_ECB);
        $iv = mcrypt_create_iv($iv_size, MCRYPT_RAND);
        $decrypted = mcrypt_decrypt(MCRYPT_RIJNDAEL_256, $key, base64_decode($code), MCRYPT_MODE_ECB, $iv);
        return $decrypted;
